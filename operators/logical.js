console.log("=== Logical Operators ===");

// AND &&
console.log("true && 5 =", true && 5);
console.log("false && 5 =", false && 5);
console.log("'hello' && 10 =", "hello" && 10);
console.log("0 && 'test' =", 0 && "test");

// OR ||
console.log("0 || 5 =", 0 || 5);
console.log("'' || 'hello' =", "" || "hello");
console.log("false || 10 =", false || 10);

// NOT !
console.log("!true =", !true);
console.log("!0 =", !0);
console.log("!'hey' =", !"hey");

// double !!
console.log("!!'hi' =", !!"hi");
console.log("!!0 =", !!0);
