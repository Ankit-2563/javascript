console.log("=== Arithmetic Operators ===");

console.log("10 + 5 =", 10 + 5);
console.log('"Hello " + "World" =', "Hello " + "World");
console.log("10 + '5' =", 10 + "5");

console.log("10 - 5 =", 10 - 5);
console.log('"10" - 5 =', "10" - 5);

console.log("10 * 2 =", 10 * 2);
console.log("10 / 2 =", 10 / 2);
console.log("10 / 0 =", 10 / 0);

console.log("10 % 3 =", 10 % 3);
console.log("2 ** 3 =", 2 ** 3);

console.log('+"5" =', +"5");
console.log('-"5" =', -"5");
console.log("+true =", +true);
console.log("+false =", +false);
